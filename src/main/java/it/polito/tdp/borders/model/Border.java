package it.polito.tdp.borders.model;

public class Border {

	private int code_1;
	private int code_2;
	
	
	
	public Border(int code_1, int code_2) {
		super();
		this.code_1 = code_1;
		this.code_2 = code_2;
	}
	public int getCode_1() {
		return code_1;
	}
	public void setCode_1(int code_1) {
		this.code_1 = code_1;
	}
	public int getCode_2() {
		return code_2;
	}
	public void setCode_2(int code_2) {
		this.code_2 = code_2;
	}
	
	
	
	
}
